int	ft_atoi(char *str)
{
	int a;
	int b;
	int c;

	a = 0;
	b = 1;
	c = 0;
	while (str[a] == '\t' || str[a] == '\v' || str[a] == '\n' ||
			str[a] == '\r' || str[a] == '\f' || str[a] == ' ')
	{
		a++;
	}
	while (str[a] == '+' || str[a] == '-')
	{
		if (str[a] == '-')
			b = b * -1;
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9' && str[a] != '\0')
	{
		c = c * 10;
		c = c + str[a] - 48;
		a++;
	}
	return (c * b);
}
