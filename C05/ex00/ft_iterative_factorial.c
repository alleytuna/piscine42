int		ft_iterative_factorial(int nb)
{
	int j;
	int i;

	j = nb;
	i = 1;
	if (nb < 0)
		return (0);
	if (j == 1 || j == 0)
		return (1);
	while (nb > i)
	{
		j = j * (nb - i);
		i++;
	}
	return (j);
}
